package JavaDSABootcamp.Recursion.RecursionSubsetSubsequenceStringQuestions;

public class Q6_PrintASCIIValueOfACharacter {
    public static void main(String[] args) {
        char ch = 'a';
        System.out.println(ch + 0); //Prints 97
        System.out.println((char) (ch + 0)); //Prints a
        System.out.println((char) (ch + 1)); //Prints b
    }
}
